// Operators


let num1 = 10;
let num2 = 3;

// for addition
console.log("Addition " + num1 + num2);

// for subtraction
console.log("Subtraction " + num1 - num2);

// for multiplication
console.log("Multiplication " + num1 * num2);

// for division
console.log("Division " + num1 / num2);

// for modulus
console.log("Modulus " + num1 % num2);
// Remaning value after division

// for exponentiation
console.log("Exponentiation " + num1 ** num2);
// num1 to the power of num2

// for increment
console.log("Increment " + ++num1); // num1 = num1 + 1

// for decrement
console.log("decrement " + --num1); // num1 = num1 - 1

// for assignment
console.log("Assignment " + num1 = num2);

// for comparison
console.log("Comparison " + num1 == num2);

// for equality
console.log("Equality " + num1 === num2);

// for inequality
console.log("Inequality " + num1 != num2);

// for strict inequality
console.log("Strict Inequality " + num1 !== num2);

// for greater than
console.log("Greater than " + num1 > num2);

// for less than
console.log("Less than " + num1 < num2);

// for greater than or equal to
console.log("Greater than or equal to " + num1 >= num2);

// for less than or equal to
console.log("Less than or equal to " + num1 <= num2);

// for logical AND
console.log("Logical AND " + num1 && num2);

// for logical OR
console.log("Logical OR " + num1 || num2);

// for logical NOT
console.log("Logical NOT " + !num1);

// for bitwise AND
console.log("Bitwise AND " + num1 & num2);

// for bitwise OR
console.log("Bitwise OR " + num1 | num2);

// for bitwise XOR
console.log("Bitwise XOR " + num1 ^ num2);

// for bitwise NOT
console.log("Bitwise NOT " + ~num1);

// for left shift
console.log("Left shift " + num1 << num2);

// for right shift
console.log("Right shift " + num1 >> num2);

// for zero-fill right shift
console.log("Zero-fill right shift " + num1 >>> num2);

// for ternary
console.log("Ternary " + (num1 > num2 ? num1 : num2));



